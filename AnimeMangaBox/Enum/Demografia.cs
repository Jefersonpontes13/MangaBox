namespace MangaBox.Enum
{
    public enum Demografia
    {
        Kodomo = 1,
        // Títulos desta demografia são direcionados para crianças num geral
        
        Shoujo = 2,
        // São títulos que tem como público-alvo garotas adolescentes.
        
        Josei = 3,
        // Diferente do shoujo, essa demografia costuma trazer romances mais bem elaborados e bem desenvolvidos.
        
        Shounen = 4,
        // Com foco em atrair os garotos adolescentes,
        // esta demografia é a que provavelmente abrange a maior parte dos desenhos japoneses.
        
        Seinen = 5
        // A demografia seinen é direcionada a homens adultos (20 anos ou mais).
    }
}